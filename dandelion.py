from turtle import *
ht()
speed(-10)
bgcolor("lightblue")
pensize(5)
pencolor("green")
right(90)
forward(200)
for i in range(41):
    pu()
    goto(0,5)
    pd()
    left(80)
    pencolor("white")
    pensize(1)
    forward(100)
    begin_fill()
    fillcolor("white")
    pensize(3)
    right(90)
    circle(5)
    end_fill()
pu()
home()
goto(0,-5)
pencolor("yellow")
pd()
begin_fill()
fillcolor("yellow")
circle(10)
end_fill()
mainloop()


